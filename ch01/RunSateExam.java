package ch01;

public class RunSateExam {

	public static void main(String[] args) {
		
		int x = 1 ; // 정수타입의 x변수에 1을 넣는다.
		int y = 2 ; // 정수타입의 y변수에 2를 넣는다.
		int result = x + y ;
		//  정수타입의 result 변수에 x변수와 y변수를 더해 넣는다.
		
		System.out.println("x + y =" + result);
		// Syout ctrl + spacebar
		// console 창에 결과를 출력한다.
		

	} //main 메서드 종료

} // RunStateExam 클래스 종료 
